#include "../../includes/minishell.h"

int	len_of_parsed(char *s)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (s[i] != '\0')
	{
		if (s[i] == '|' || s[i] == '<' || s[i] == '>')
		{
			while ((s[i] == '|' || s[i] == '<' || s[i] == '>') && s[i] != '\0')
				i ++;
			j ++;
		}
		if (s[i] != '|' && s[i] != '<' && s[i] != '>' && s[i] != '\0')
		{
			while (s[i] != '|' && s[i] != '<' && s[i] != '>' && s[i] != '\0')
				i ++;
			j ++;
		}
	}
	return (j);
}
